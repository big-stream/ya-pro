# 基礎概念その6: 計算


# 足し算や引き算は、算数と同じ。
答え = 1 + 2 - 3

# 掛け算はアスタリスク、割り算はスラッシュを使う。
答え = 1 * 2 / 2

# 余りの計算はパーセントを使う。
# 3÷2の余りがいくつか
答え = 3 % 2
print(答え)


# 計算をまとめるときの括弧
答え = 2 * (1 + 1)
print(答え)


# スラッシュ一つで小数点表示、スラッシュ二つで、整数表示になる。
答え = 4 / 2
print(答え)
答え = 4 // 2
print(答え)

