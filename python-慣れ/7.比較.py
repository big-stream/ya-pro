# 基礎概念その7: 比較


# 文字列の比較には、==を使う。

# その結果、同じだったら、True、違ったら、False
print('あいうえお' == 'あいうえお')
print('あいうえお' == 'アイウエオ')


# 数値の比較:
# a == b: aとbは等しいか
# a < b: aはbより小さいか
# a > b: aはbより大きいか
# a <= b: aはb以下か
# a >= b: aはb以上か

print(1 == 2)
print(1 < 2)

