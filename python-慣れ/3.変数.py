# 基礎概念その3: 変数


# プログラムで使うデータは、入れ物に名前を付けてしまっておく。
# データの入れ物のことを、変数という。

人数 = 10
どうぶつ = 'キリン'
あいさつ = 'こんにちは'
挨拶3 = 'どうも'
cat_name = 'ミケ'


# データを入れるとき、イコールを使う。
なまえ = 'アリス'

# 特に、同じデータを何回も使う時は、変数に入れよう。
print(なまえ + 'さんが、現れた')

print(なまえ + 'さん、おはよう')
print(なまえ + 'さん、こんにちは')

